package Strings;

public class Range {
	private int first;
	private int last;
	
	public Range(int first, int last)
	{
		super();
		this.first = first;
		this.last = last;
	}
	public int getFirst() {
		return first;
	}
	public void setFirst(int first) {
		this.first = first;
	}
	public int getLast() {
		return last;
	}
	public void setLast(int last) {
		this.last = last;
	}
	
	
}
